package com.wy.test.core.constant;

public interface ConstTimeInterval {

	int ONE_MINUTE = 60;

	int ONE_HOUR = 60 * 60;

	int ONE_DAY = 60 * 60 * 24;

	int ONE_WEEK = ONE_DAY * 7;

	int TWO_WEEK = ONE_DAY * 14;

	int ONE_MONTH = ONE_DAY * 30;

	int TWO_MONTH = ONE_DAY * 60;

	int ONE_YEAR = 60 * 60 * 24 * 365;
}